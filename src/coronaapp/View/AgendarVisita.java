    private void btnAgendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarActionPerformed
        int selecionado = listPacientes.getSelectedIndex();
        
        if (selecionado >= 0) {
            Date date = (Date) spinnerDate.getValue();
            String cpfPaciente = Login.pacientesInstanciados.get(selecionado).getCpf();
            equipeMedica.agendarVisita(date, cpfPaciente);            
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um paciente primeiramente", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgendarActionPerformed

    private void visitasAgendadasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_visitasAgendadasActionPerformed
        DefaultTableModel dataModel = new DefaultTableModel();
        ArrayList<String> listCpfPacientes = new ArrayList<>();
        ArrayList<Date> listDate = new ArrayList<>();
        for (Visita item : coronaapp.CoronaApp.visitasAgendadas){
            listCpfPacientes.add(item.getCpfPaciente());
            listDate.add(item.getData());
        }
        dataModel.addColumn("Paciente",listCpfPacientes.toArray());
        dataModel.addColumn("Data", listDate.toArray());
        
        VisualizarVisitas visualizarVisitas = new VisualizarVisitas(this, dataModel);
        visualizarVisitas.setLocationRelativeTo(this);
        visualizarVisitas.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_visitasAgendadasActionPerformed

