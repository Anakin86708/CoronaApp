    private void listarPacientes() {
        DefaultListModel<String> model = new DefaultListModel<>();
        for (Paciente nomePaciente : Login.pacientesInstanciados) {
            model.addElement(nomePaciente.getNome());
        }
        listPacientes.setModel(model);
        listPacientes.updateUI();
    }

